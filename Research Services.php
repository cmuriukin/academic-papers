<p>State the thesis- a thesis should primarily not be more than one or two sentences. It should basically embody the beliefs of a writer concerning the topic he/she is writing on. The thesis will be supported by the information in the research paper.</p>
<p>Drawing an outline- In making an outline, one should first reflect through their topic, and then put in logical order the informational items that will be contained in the research paper. Having a good outline is the key to writing a good research paper.</p>
<p>Writing the research paper- writing the research paper requires that one take the ideas stated in their outline and expound on them. One must make sure that their research paper contains all the information required to meet the criteria of their lecturer/professor or other relevant parties.</p>
<p>Proof reading - this is the final step before finally printing the research paper. It requires one to peruse through the paper, trying to identify any grammatical errors, punctuation mistakes, and other irrelevant information. If possible, a second party should be allowed to look at the research paper before printing for they might find a mistake that was initially looked over.</p>
